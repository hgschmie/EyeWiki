package de.softwareforge.eyewiki;

/**
 * A generic Wiki exception.
 *
 * @author Janne Jalkanen
 *
 * @since 2.0
 */
public class WikiException
        extends Exception
{
    /**
     * Constructs an exception.
     *
     * @param msg DOCUMENT ME!
     */
    public WikiException(String msg)
    {
        super(msg);
    }
}
